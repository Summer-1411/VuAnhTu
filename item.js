function showThongTin(event)  {
    var ShowThongTinItem = document.getElementById("ShowThongTinItem");
    var heightValue = ShowThongTinItem.style.height;
    if(heightValue !== "1090px") {
        ShowThongTinItem.style.height = "1090px";
    } else {
        ShowThongTinItem.style.height = "auto";
    }
 }

